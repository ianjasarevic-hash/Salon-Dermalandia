document.addEventListener('DOMContentLoaded', () => {
    // Navbar Scroll Effect
    const navbar = document.querySelector('.navbar');
    window.addEventListener('scroll', () => {
        if (window.scrollY > 50) {
            navbar.classList.add('scrolled');
        } else {
            navbar.classList.remove('scrolled');
        }
    });

    // Language Switcher Logic
    const langBtns = document.querySelectorAll('.lang-btn');
    const translatableNodes = document.querySelectorAll('[data-i18n]');

    function setLanguage(lang) {
        window.currentLang = lang; // Track globally for the modals
        // Update active class on buttons
        langBtns.forEach(btn => {
            if (btn.dataset.lang === lang) {
                btn.classList.add('active');
            } else {
                btn.classList.remove('active');
            }
        });

        // Update text across DOM
        translatableNodes.forEach(node => {
            const key = node.dataset.i18n;
            if (translations[lang] && translations[lang][key]) {
                node.innerHTML = translations[lang][key];
            }
        });

        // Save preference
        localStorage.setItem('dermalandia_lang', lang);
    }

    // Attach click events
    langBtns.forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.dataset.lang);
        });
    });

    // Check saved language or default to 'sl'
    const savedLang = localStorage.getItem('dermalandia_lang') || 'sl';
    setLanguage(savedLang);
});

// Modal Logic
const modal = document.getElementById('serviceModal');
const modalTitle = document.getElementById('modalTitle');
const modalDesc = document.getElementById('modalDesc');
const modalIcon = document.getElementById('modalIcon');

// Icon map for the modal
const iconMap = {
    'cat_1': 'fa-star',
    'cat_2': 'fa-hand-holding-heart',
    'cat_3': 'fa-face-smile-beam',
    'cat_4': 'fa-wand-magic-sparkles',
    'cat_5': 'fa-dna',
    'cat_6': 'fa-leaf',
    'cat_7': 'fa-eye',
    'cat_8': 'fa-hand-sparkles',
    'cat_9': 'fa-wind',
    'cat_10': 'fa-shoe-prints'
};

function openModal(serviceKey) {
    if (!modal) return;

    // Fallback if currentLang isn't caught yet
    const activeLang = window.currentLang || 'sl';

    // Set text based on current language
    modalTitle.innerHTML = translations[activeLang][serviceKey];
    modalDesc.innerHTML = translations[activeLang][serviceKey + '_desc'];

    // Set correct icon
    modalIcon.className = `fa-solid ${iconMap[serviceKey]} card-icon`;

    // Manage Global Button visibility & Modal Theme for specific categories
    const modalBookBtn = document.getElementById('modalBookBtn');
    const modalContent = modal.querySelector('.modal-content');

    if (serviceKey === 'cat_8' || serviceKey === 'cat_1' || serviceKey === 'cat_2' || serviceKey === 'cat_3' || serviceKey === 'cat_4' || serviceKey === 'cat_5' || serviceKey === 'cat_6' || serviceKey === 'cat_7' || serviceKey === 'cat_9' || serviceKey === 'cat_10') {
        if (modalBookBtn) modalBookBtn.style.display = 'none';
        modalTitle.style.marginBottom = '0';
        if (modalContent) modalContent.classList.add('dark-theme');
    } else {
        if (modalBookBtn) modalBookBtn.style.display = 'inline-block';
        modalTitle.style.marginBottom = '1rem';
        if (modalContent) modalContent.classList.remove('dark-theme');
    }

    // Show modal
    modal.classList.add('active');
    document.body.style.overflow = 'hidden'; // Prevent background scrolling
}

function closeModal() {
    if (!modal) return;
    modal.classList.remove('active');
    document.body.style.overflow = ''; // Restore scrolling
}

// Close when clicking outside of the modal content
window.addEventListener('click', (e) => {
    if (e.target === modal) {
        closeModal();
    }
});

// Programmatic Event Listeners for Service Cards to bypass inline constraints
document.querySelectorAll('.glass-card').forEach((card, index) => {
    const serviceKey = 'cat_' + (index + 1);
    card.addEventListener('click', (e) => {
        // Prevent opening the external booking link if clicking the button on the card
        if (e.target.tagName.toLowerCase() === 'a' || e.target.closest('a')) {
            e.preventDefault();
        }
        openModal(serviceKey);
    });
});
